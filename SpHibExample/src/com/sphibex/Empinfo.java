package com.sphibex;

public class Empinfo {
	private int empid;
	private String empmame;
	private String empdept;
	private int empsal;
	
	public int getEmpid() {
		return empid;
	}
	public void setEmpid(int empid) {
		this.empid = empid;
	}
	public String getEmpmame() {
		return empmame;
	}
	public void setEmpmame(String empmame) {
		this.empmame = empmame;
	}
	public String getEmpdept() {
		return empdept;
	}
	public void setEmpdept(String empdept) {
		this.empdept = empdept;
	}
	public int getEmpsal() {
		return empsal;
	}
	public void setEmpsal(int empsal) {
		this.empsal = empsal;
	}

}
